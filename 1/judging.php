            <p class="bold">How our medals are graded:
                <ul>
                    <li>Platinum: 96 points or higher</li>
                    <li>Gran d’Or (Double Gold): 93 to 95.99 points</li>
                    <li>Gold: 89 to 92.99 points</li>
                    <li>Silver: 86 to 88.99 points</li>
                </ul>
            </p>
            <p>No bronze medals or merit awards are awarded.</p>
