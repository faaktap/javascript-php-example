            the Michelangelo International Wine & Spirits Awards will be eligible for medals,
            and are calculated according to the judges’ final scores.</p>
            <p>Incorrect entries are removed, corrected and if necessary- re-tasted by another panel
            of judges.</p>
            <p>Only an estimated top one third of entries are awarded a medal.</p>
            <p>Results presented for public announcement and publication were not manipulated in
                any way.</p>
