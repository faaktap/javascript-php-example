                Now, in its 24th year, the competition remains
                unique in South Africa, in that all judges are hand-picked wine experts,
                representing all seven continents. Since the start of the competition in 1997,
                more than 170 different judges from 46 countries (more recently including
                Russia, Romania and China) have served on the panel. All entries are judged
                blind by panels of 5 or 6 judges, using the 100-point international recognised
                OIV judging system.
