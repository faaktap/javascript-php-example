                    <div id="Craft" class="tabcontent">
                        <h3><strong>Craft Spirits</strong></h3>
                        <strong>DEFINITION:</strong>
                        <p>A craft spirit is distilled by a craft spirit producer using only
                            natural products to achieve the end product’s sensory profile.</p>
                        <p>A craft spirit producer is a distillery that complies with all the
                            following criteria:</p>
                        <ul class="ms-3">
                            <li>Produces end-products containing less than 100,000L of absolute
                                alcohol per annum.</li>
                            <li>Distils more than 50% of its annual production of absolute
                                alcohol, from self-fermented raw material, except in the case of
                                gin production, where re-distillation from neutral spirits is
                                allowed.</li>
                            <li>Is majority-owned by natural persons.</li>
                            <li>A distiller with more than 25% equity in the distillery is in
                                direct control of the fermentations, distilling and final
                                blending/mixing.</li>
                            <li>No process integration computer-based control systems are
                                utilised in the distillery.</li>
                            <li>All process steps in the production of the end product (i.e.
                                fermentation, distilling, blending/mixing, infusion, bottling
                                and labelling) must be performed on the same premises.</li>
                        </ul>
                    </div>

